pi = 3.14
radius = float(input("Enter The Sphere's Radius: "))
diameter = radius * 2
print("The Diameter Of The Sphere Is", diameter, ".")
circumference = pi * diameter
print("The Circumference Of The Sphere Is", circumference, ".")
area = 4 * pi * (radius ** 2)
print("The Surface Area Of The Sphere Is", area, ".")
volume = (4 / 3) * pi * (radius ** 2)
print("The Volume Of The Sphere Is", volume, ".")
